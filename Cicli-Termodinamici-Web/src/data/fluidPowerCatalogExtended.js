const makeActuator = ({
  id,
  domain,
  label,
  description,
  symbol,
  symbolVariant,
  defaultSize = { width: 176, height: 104 },
  ports = [],
  actuatorType = 'double',
  workPorts = [],
}) => ({
  id,
  domain,
  category: 'utilizzatori',
  label,
  description,
  symbol,
  symbolVariant,
  defaultSize,
  ports,
  simBehavior: {
    kind: 'actuator',
    actuatorType,
    workPorts,
  },
});

const makeValve = ({
  id,
  domain,
  label,
  description,
  family,
  states,
  returnPorts = [],
  defaultSize = { width: 176, height: 112 },
  symbol = null,
  symbolVariant = null,
}) => {
  const workPorts = family === '2/2' || family === '3/2' ? ['A'] : ['A', 'B'];

  return {
    id,
    domain,
    category: 'valvole-distributrici',
    label,
    description,
    symbol: symbol ?? `directional-${family.replace('/', '-')}`,
    symbolVariant,
    defaultSize,
    ports: [
      { id: 'P', label: 'P', side: 'left', align: 0.5, kind: 'fluid' },
      { id: 'A', label: 'A', side: 'right', align: 0.3, kind: 'fluid' },
      ...(family === '2/2' || family === '3/2'
        ? []
        : [{ id: 'B', label: 'B', side: 'right', align: 0.72, kind: 'fluid' }]),
      ...returnPorts.map((portId, index) => ({
        id: portId,
        label: portId,
        side: 'bottom',
        align: returnPorts.length === 1 ? 0.5 : index === 0 ? 0.35 : 0.68,
        kind: 'fluid',
      })),
    ],
    simBehavior: {
      kind: 'valve',
      family,
      supplyPort: 'P',
      workPorts,
      returnPorts,
      states,
      defaultState: states[0]?.id ?? null,
    },
  };
};

